public class IfTest3 {
  public static void main(String[] args) {
    int myAge = 24;
    if (myAge == 25) {
      System.out.println("私の年齢は２５歳です！");
    } else if (myAge >= 30) {
      System.out.println("私の年齢は３０歳以上です！");
    } else {
      System.out.println("私の年齢は２５歳でも３０歳以上でもありません！");
    }
    System.out.println("私の出身地は・・・");
  }
}
